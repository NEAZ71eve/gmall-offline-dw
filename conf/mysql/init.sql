-- ============================================================
-- gmall 电商数据库初始化
-- 业务源表 + 模拟数据
-- ============================================================

-- 1. 用户信息表
CREATE TABLE IF NOT EXISTS user_info (
    id VARCHAR(64) PRIMARY KEY,
    login_name VARCHAR(255),
    nick_name VARCHAR(255),
    name VARCHAR(255),
    phone_num VARCHAR(32),
    email VARCHAR(255),
    head_img VARCHAR(512),
    user_level VARCHAR(32),
    birthday VARCHAR(32),
    gender VARCHAR(4),
    create_time VARCHAR(32),
    operate_time VARCHAR(32),
    status VARCHAR(8)
) COMMENT '用户信息表';

-- 2. SKU 信息表
CREATE TABLE IF NOT EXISTS sku_info (
    id VARCHAR(64) PRIMARY KEY,
    spu_id VARCHAR(64),
    price DECIMAL(16,2),
    sku_name VARCHAR(255),
    sku_desc TEXT,
    weight DECIMAL(10,2),
    tm_id VARCHAR(64),
    category3_id VARCHAR(64),
    create_time VARCHAR(32),
    category1_id VARCHAR(64),
    category2_id VARCHAR(64),
    category1_name VARCHAR(255),
    category2_name VARCHAR(255),
    category3_name VARCHAR(255),
    tm_name VARCHAR(255)
) COMMENT 'SKU信息表';

-- 3. 订单信息表
CREATE TABLE IF NOT EXISTS order_info (
    id VARCHAR(64) PRIMARY KEY,
    consignee VARCHAR(255),
    consignee_tel VARCHAR(64),
    total_amount DECIMAL(16,2),
    order_status VARCHAR(32),
    user_id VARCHAR(64),
    payment_way VARCHAR(32),
    delivery_address TEXT,
    order_comment TEXT,
    out_trade_no VARCHAR(128),
    trade_body VARCHAR(512),
    create_time VARCHAR(32),
    operate_time VARCHAR(32),
    expire_time VARCHAR(32),
    tracking_no VARCHAR(128),
    parent_order_id VARCHAR(64),
    img_url VARCHAR(512),
    province_id VARCHAR(16),
    benefit_reduce_amount DECIMAL(16,2),
    original_total_amount DECIMAL(16,2),
    feight_fee DECIMAL(10,2),
    sku_num INT DEFAULT 1
) COMMENT '订单信息表';

-- 4. 订单明细表
CREATE TABLE IF NOT EXISTS order_detail (
    id VARCHAR(64) PRIMARY KEY,
    order_id VARCHAR(64),
    sku_id VARCHAR(64),
    sku_name VARCHAR(255),
    img_url VARCHAR(512),
    order_price DECIMAL(16,2),
    sku_num BIGINT,
    create_time VARCHAR(32),
    source_type VARCHAR(32),
    source_id VARCHAR(64),
    KEY idx_order_id (order_id),
    KEY idx_sku_id (sku_id)
) COMMENT '订单明细表';

-- 5. ADS 结果表 (供 Superset 可视化)
CREATE TABLE IF NOT EXISTS ads_gmv_day (
    stat_date VARCHAR(10),
    gmv DECIMAL(16,2),
    order_count BIGINT,
    order_user_count BIGINT,
    payment_amount DECIMAL(16,2),
    avg_order_amount DECIMAL(16,2)
) COMMENT 'GMV日报';

CREATE TABLE IF NOT EXISTS ads_order_stats (
    stat_date VARCHAR(10),
    hour_slot INT,
    order_count BIGINT,
    gmv DECIMAL(16,2)
) COMMENT '时段订单统计';

CREATE TABLE IF NOT EXISTS ads_hot_sku (
    stat_date VARCHAR(10),
    sku_id VARCHAR(64),
    sku_name VARCHAR(255),
    order_count BIGINT,
    total_gmv DECIMAL(16,2)
) COMMENT '热门商品Top10';

CREATE TABLE IF NOT EXISTS ads_user_retention (
    stat_date VARCHAR(10),
    new_user_count BIGINT,
    day1_retain BIGINT,
    day7_retain BIGINT,
    retention_rate_d1 DECIMAL(5,2),
    retention_rate_d7 DECIMAL(5,2)
) COMMENT '用户留存';

CREATE TABLE IF NOT EXISTS ads_conversion_funnel (
    stat_date VARCHAR(10),
    step_view BIGINT,
    step_add_cart BIGINT,
    step_order BIGINT,
    step_pay BIGINT,
    view_to_cart_rate DECIMAL(5,2),
    cart_to_order_rate DECIMAL(5,2),
    order_to_pay_rate DECIMAL(5,2)
) COMMENT '转化漏斗';

